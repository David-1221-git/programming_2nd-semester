package chi;
public class Vehiculo {
    public String patente;
    public String marca;

    public void setPatente(String p) {
        patente = p;
    }

    public void setMarca(String m) {
        marca = m;
    }

    public String getPatente() {
        return patente;
    }

    public String getMarca() {
        return marca;
    }
}
